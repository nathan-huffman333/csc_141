# This code is for finding what someone's favorite fruits are.

favorite_fruits = ["apple", "watermelon", "grapes"]

if "apple" in favorite_fruits:
    print("You like apples!")
if "watermelon" in favorite_fruits:
    print("You like watermelon!")
if "grapes" in favorite_fruits:
    print("You like grapes!")
if "strawberry" in favorite_fruits:
    print("You like strawberries!")
if "orange" in favorite_fruits:
    print("You like oranges!")