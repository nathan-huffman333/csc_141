locations = ["Japan", "France","Italy", "Germany","England"]
print("\n")

# original
print(locations)
print("\n")

# sorted
print(sorted(locations))
print("\n")

# original
print(locations)
print("\n")

# sorted reverse
print(sorted(locations, reverse = True))
print("\n")

# original
print(locations)
print("\n")

# reverse
locations.reverse()
print(locations)
print("\n")

# back to original
locations.reverse()
print(locations)
print("\n")

# perment sorting
locations.sort()
print(locations)
print("\n")

# sorting in reverse
locations.sort(reverse = True)
print(locations)
print("\n")